def find_already_scraped() -> set[str]:
    """Check which URLs already have scraped files."""
    done = set()
    if not OUTPUT_DIR.exists():
        return done

    for md_file in OUTPUT_DIR.glob("*.md"):
        try:
            text = md_file.read_text(encoding="utf-8", errors="replace")
            # Extract URL from header
            for line in text.splitlines()[:10]:
                if line.startswith("**URL:**"):
                    url = line.replace("**URL:**", "").strip()
                    # Only count as done if status is DONE or PARTIAL
                    if "**Scrape status:** DONE" in text or \
                       "**Scrape status:** PARTIAL" in text:
                        done.add(url)
                    break
        except Exception:
            continue

    return done


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="Bitcoin Notes URL scraping runner"
    )
    parser.add_argument(
        "--category", choices=["scrapable", "tweet", "medium", "reddit", "all"],
        default="all", help="Which URL category to scrape",
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Show plan without scraping",
    )
    parser.add_argument(
        "--resume", action="store_true",
        help="Skip URLs that already have scraped files",
    )
    parser.add_argument(
        "--limit", type=int, default=0,
        help="Limit number of URLs to scrape (0 = all)",
    )
    args = parser.parse_args()

    # Load URL data
    if not URLS_JSON.exists():
        print("Error: bitcoin-notes-urls.json not found. Run bitcoin-notes-prep.py first.")
        sys.exit(1)

    data = json.loads(URLS_JSON.read_text(encoding="utf-8"))

    # Check resume state
    already_done = set()
    if args.resume:
        already_done = find_already_scraped()
        if already_done:
            print(f"\n  Resume mode: {len(already_done)} URLs already scraped")

    # Determine which categories to run
    categories = []
    if args.category == "all":
        categories = ["scrapable", "tweet", "reddit"]
    else:
        categories = [args.category]

    print(f"\n{'#' * 60}")
    print(f"  BITCOIN NOTES SCRAPING WAVE")
    print(f"  Categories: {', '.join(categories)}")
    print(f"  Started: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print(f"{'#' * 60}")

    all_results = {}

    for cat in categories:
        key = f"{cat}_urls"
        urls = data.get(key, [])

        if args.limit > 0:
            urls = urls[:args.limit]

        if not urls:
            print(f"\n  No {cat} URLs to process.")
            continue

        try:
            results = scrape_url_list(
                urls, cat, dry_run=args.dry_run, already_done=already_done
            )
            all_results[cat] = results
        except KeyboardInterrupt:
            print(f"\n\n  Interrupted during {cat} wave. Progress saved.")
            break

    # Close browser if used
    close_browser()

    if not args.dry_run and any(all_results.values()):
        # Save log
        log_entry = {
            "wave": "bitcoin-notes",
            "timestamp": datetime.now().isoformat(),
            "categories": {
                cat: {
                    "total": len(results),
                    "done": sum(1 for r in results if r["status"] == "done"),
                    "partial": sum(1 for r in results if r["status"] == "partial"),
                    "failed": sum(1 for r in results if r["status"] == "failed"),
                    "total_chars": sum(r["chars"] for r in results),
                }
                for cat, results in all_results.items()
            },
        }

        if LOG_PATH.exists():
            log = json.loads(LOG_PATH.read_text(encoding="utf-8"))
            if not isinstance(log, list):
                log = [log]
            log.append(log_entry)
        else:
            log = [log_entry]

        LOG_PATH.write_text(
            json.dumps(log, indent=2, default=str), encoding="utf-8"
        )
        print(f"\n  Log saved: {LOG_PATH.name}")

    # Grand total
    total_done = sum(
        sum(1 for r in results if r["status"] == "done")
        for results in all_results.values()
    )
    total_chars = sum(
        sum(r["chars"] for r in results)
        for results in all_results.values()
    )

    print(f"\n{'#' * 60}")
    print(f"  GRAND TOTAL: {total_done} URLs scraped, {total_chars:,} chars")
    print(f"  Output: {OUTPUT_DIR}")
    print(f"{'#' * 60}")


if __name__ == "__main__":
    main()
