"""
Bitcoin Notes URL scraping runner (btc-index Phase 3B).

Reads URL lists generated by bitcoin-notes-prep.py, scrapes each URL
using the shared scraper infrastructure, and writes individual .md files
to btc-index/scraped/ for later corpus ingestion.

Usage:
    python run-bitcoin-notes.py                    # Scrape all categories
    python run-bitcoin-notes.py --category scrapable  # Articles only
    python run-bitcoin-notes.py --category tweet      # Tweets only
    python run-bitcoin-notes.py --dry-run             # Show plan
    python run-bitcoin-notes.py --resume              # Skip already-scraped URLs

Run from btc-index/scraper/:
    ../mcp/.venv/Scripts/python.exe run-bitcoin-notes.py
"""

